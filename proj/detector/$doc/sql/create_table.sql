-- USE `AISLE_DETECTOR_DB`;

DROP TABLE IF EXISTS `T_NUMERIC`;
CREATE TABLE `T_NUMERIC` (

	`sys_id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '系统标识',
	`sys_create_time` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间戳',
	`c_tinyint` TINYINT DEFAULT 0 COMMENT '1|(-128,127)|(0,255)',
	`c_smallint` SMALLINT DEFAULT 0 COMMENT '2|(-32 768,32 767)|(0,65 535)',
	`c_mediumint` MEDIUMINT DEFAULT 0 COMMENT '3|(-8 388 608,8 388 607)|(0,16 777 215)',
	`c_int` INT DEFAULT 0 COMMENT '4|(-2 147 483 648,2 147 483 647)|(0,4 294 967 295)',
	`c_bigint` BIGINT DEFAULT 0 COMMENT '8|(-9 233 372 036 854 775 808,9 223 372 036 854 775 807)|(0,18 446 744 073 709 551 615)',
	`c_float` FLOAT DEFAULT 0 COMMENT '4|(-3.402 823 466 E+38,1.175 494 351 E-38),0,(1.175 494 351 E-38,3.402 823 466 351 E+38)|0,(1.175 494 351 E-38,3.402 823 466 E+38)',
	`c_double` DOUBLE DEFAULT 0 COMMENT '8|(1.797 693 134 862 315 7 E+308,2.225 073 858 507 201 4 E-308),0,(2.225 073 858 507 201 4 E-308,1.797 693 134 862 315 7 E+308)|0,(2.225 073 858 507 201 4 E-308,1.797 693 134 862 315 7 E+308)',
	`c_decimal` DECIMAL(6,2) DEFAULT 0 COMMENT 'DECIMAL(M,D) ,如果M>D,为M+2否则为D+2|依赖于M和D的值|依赖于M和D的值',
	
	PRIMARY KEY (`sys_Id`)

) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT 'numeric data type test for detector.';

DROP TABLE IF EXISTS `T_DATETIME`;
CREATE TABLE `T_DATETIME` (

	`sys_id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '系统标识',
	`sys_create_time` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间戳',
	`c_date` DATE DEFAULT 0 COMMENT '3|1000-01-01/9999-12-31|YYYY-MM-DD',
	`c_time` TIME DEFAULT 0 COMMENT '3|-838:59:59/838:59:59|HH:MM:SS',
	`c_year` YEAR DEFAULT 0 COMMENT '1|1901/2155|YYYY',
	`c_datetime` DATETIME DEFAULT 0 COMMENT '8|1000-01-01 00:00:00/9999-12-31 23:59:59|YYYY-MM-DD HH:MM:SS',
	`c_timestamp` TIMESTAMP DEFAULT 0 COMMENT '8|1970-01-01 00:00:00/2037-12-31 23:59:59|YYYYMMDD HHMMSS',
	
	PRIMARY KEY (`sys_Id`)

) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT 'datetime data type test for detector.';


DROP TABLE IF EXISTS `T_STRING`;
CREATE TABLE `T_STRING` (

	`sys_id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '系统标识',
	`sys_create_time` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间戳',
	`c_char` CHAR(10) DEFAULT NULL COMMENT '0~255',
	`c_varchar` VARCHAR(10) DEFAULT NULL COMMENT '0~255',
	`c_tinytext` TINYTEXT DEFAULT NULL COMMENT '0~255',
	`c_text` TEXT DEFAULT NULL COMMENT '0~65 535',
	`c_mediumtext` MEDIUMTEXT DEFAULT NULL COMMENT '0~16 777 215',
	`c_logtext` LONGTEXT DEFAULT NULL COMMENT '0~4 294 967 295',
	`c_enum` ENUM('e1','e2','e3') DEFAULT NULL COMMENT '0~65 535',
	`c_set` SET('s1','s2','s3') DEFAULT NULL COMMENT '0~64',
	
	PRIMARY KEY (`sys_Id`)

) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT 'string data type test for detector.';


DROP TABLE IF EXISTS `T_BINARY`;
CREATE TABLE `T_BINARY` (

	`sys_id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '系统标识',
	`sys_create_time` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间戳',
	`c_bit` BIT(1) DEFAULT 0 COMMENT '0~64',
	`c_binary` BINARY(10) DEFAULT NULL COMMENT '0~x',
	`c_varbinary` VARBINARY(10) DEFAULT NULL COMMENT '0~x+1',
	`c_tinyblob` TINYBLOB DEFAULT NULL COMMENT '0~255',
	`c_blob` BLOB DEFAULT NULL COMMENT '0~2^16-1',
	`c_mediumblob` MEDIUMBLOB DEFAULT NULL COMMENT '0~2^24-1',
	`c_longblob` LONGBLOB DEFAULT NULL COMMENT '0~2^32-1',
	
	PRIMARY KEY (`sys_Id`)

) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT 'binary data type test for detector.';



